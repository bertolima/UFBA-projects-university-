import random

# ***********************************************************************************************************************
# *---------------------------------------------------------------------------------------------------------------------*
# ***********************************************************************************************************************

class Carta: #Essa CLASS funciona para separar as cartas em peso e o naipe, ela cria objetos de cartas que vem do Baralho;
             #Ela será direcionada aos jogadores.    

    def __init__(self, peso, naipe):
        self.peso = peso
        self.naipe = naipe

# ***********************************************************************************************************************
# *---------------------------------------------------------------------------------------------------------------------*
# ***********************************************************************************************************************

class Baralho: #Essa classe é utilizada para simular o baralho do jogo, em seguida temos as representações das cartas do baralho
    NAIPES = ["Copas", "Espadas", "Paus", "Ouros"]
    PESOS = ['Ás', '2', '3', '4', '5', '6', '7', '8', '9', '10', 'Valete', 'Dama', 'Rei']
# ***********************************************************************************************************************
    def __init__(self): #Aqui atribuo cartas com pesos e naipes:
        self.cartas = [Carta(naipe, peso) for naipe in self.NAIPES for peso in self.PESOS]
# ***********************************************************************************************************************  
    def embaralhar(self): #Dessa forma, tendo as cartas, basta embaralhar as recebidas por cada jogador;
        random.shuffle(self.cartas)
# *********************************************************************************************************************** 
    def retirar_carta(self): #Nessa lista, é retirado o último elemento dela e retorna esse elemento. No caso, retira a carta do topo do baralho;
        return self.cartas.pop() 
